# 使い方：各ブロックを1つずつコメントアウトを外して実行する
#         エラーメッセージを読んで、原因を自分で考えてから
#         下の「原因」を確認すること
# ============================================================


# ============================================================
# 1. SyntaxError（文法ミス）
# ============================================================

# 以下の2行のコメント(#)を外して実行してみる

# if True
#     print("hello")

# 原因：ifの行末にコロン(:)がない
# 修正：if True: にする


# ============================================================
# 2. NameError（存在しない変数を使った）
# ============================================================

# print(undefined_variable)

# 原因：undefined_variable という変数を定義していないのに使おうとした
# 修正：先に undefined_variable = "何か" と定義する


# ============================================================
# 3. TypeError（型が合っていない）
# ============================================================

# result = "年齢は" + 25
# print(result)

# 原因：文字列と数値は + で結合できない
# 修正：str(25) で文字列に変換してから結合する


# ============================================================
# 4. IndexError（リストの範囲外）
# ============================================================

# numbers = [1, 2, 3]
# print(numbers[5])

# 原因：要素は3つ(インデックス0〜2)なのに5番目を指定した
# 修正：print(numbers[2]) にする（最後の要素）


# ============================================================
# 5. KeyError（辞書に存在しないキーを指定した）
# ============================================================

# person = {"name": "太郎", "age": 25}
# print(person["email"])

# 原因："email" というキーが辞書に存在しない
# 修正：person.get("email", "未登録") で存在しない場合のデフォルト値を指定する


# ============================================================
# 6. FileNotFoundError（ファイルが見つからない）
# ============================================================

# with open("存在しないファイル.txt", "r") as f:
#     content = f.read()

# 原因：指定したファイルが存在しない
# 修正：ファイルパスを正しいものにするか、try-exceptで処理する


# ============================================================
# 7. ZeroDivisionError（0で割った）
# ============================================================

# result = 10 / 0
# print(result)

# 原因：数学と同じく0での除算はできない
# 修正：割る前に if divisor != 0: で確認する


# ============================================================
# 8. AttributeError（存在しないメソッドを呼んだ）
# ============================================================

# number = 42
# number.upper()

# 原因：upper()は文字列のメソッドで、数値には存在しない
# 修正："hello".upper() のように文字列に対して使う


# ============================================================
# 9. ImportError（存在しないライブラリをインポート）
# ============================================================

# import non_existent_library

# 原因：そのようなライブラリはインストールされていない
# 修正：pip install ライブラリ名 でインストールしてから使う


# ============================================================
# 10. 例外処理の練習（try-except）
# ============================================================
# エラーが出ても止まらないようにするのが例外処理
# 以下はコメントアウトを外してそのまま動く

# try:
